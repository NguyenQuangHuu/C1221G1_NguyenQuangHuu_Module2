package ss1_introduction_java.thuc_hanh;

public class DataType {
    public static void main(String[] args) {
        int i = 10; //Khai báo biến i kiểu int có giá trị là 10
        float f = 20.5f;//Khai báo biến f kiểu float có giá trị 20.5
        double d = 20.5; // Khai báo biến d có kiểu là double và giá trị = 20.5
        boolean b = true; //Khai báo biến b có kiểu boolean và giá trị là true;
        char c = 'a'; //Khai báo biến c có kiểu char và giá trị là kí tự a

        String s = "Đà Nẵng"; //Khai báo biens s có kiểu String và giá trị là chữ Đà Nẵng

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("d = " + d);
        System.out.println("b = " + b);
        System.out.println("c = " + c);
        System.out.println("s = " + s);
    }
}
